import React, { useState } from "react";
import Head from "next/head";
import { useSelector } from "react-redux";
import CardList from "../components/CardList";
import SearchBox from "../components/SearchBox";
import { Data } from "../models";

export default function Series() {
  const [searchQuery, setSearchQuery] = useState("");

  const { movies } = useSelector((state: any) => state.movies);
  const allTvSeries = movies.filter(
    (item: any) => item.category === "TV Series"
  );

  const filteredSeries = movies.filter((element: Data) =>
    element.title.toLowerCase().includes(searchQuery)
  );

  return (
    <div>
      <Head>
        <title>TV Series</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="bg-slate-900 text-white min-h-screen">
        <div className="py-28 lg:pt-4 lg:pl-32">
          <SearchBox
            placeholder="Search for TV Series"
            setSearchQuery={setSearchQuery}
          />

          {searchQuery ? (
            <CardList
              title={`Found ${filteredSeries.length} ${
                filteredSeries.length > 1 ? "results" : "result"
              } for '${searchQuery}'`}
              films={filteredSeries}
            />
          ) : (
            <>
              <CardList title="Movies" films={allTvSeries} />
            </>
          )}
        </div>
      </main>
    </div>
  );
}
